Дана переменная var1, в которой хранится целое число. Создайте и напечатайте новую переменную var2, 
равную var1 умножить на 10.

В R чтобы напечатать значение переменной, достаточно написать ее имя в консоль. 
Однако в целях упрощения проверки заданий мы просим вас  
(кроме отдельно оговоренных случаев) печатать значение переменных при помощи команды print().

var2 <- var1 * 10
print(var2)
